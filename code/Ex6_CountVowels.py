# Ex6: Counting vowels

s = input('Enter your name: ') # Takes as input a string representing a user's name
s = s.lower()
print('Length of your name is: ', len(s))
print("The number of a's are ", s.count('a'))
print("The number of e's are ", s.count('e'))
print("The number of i's are ", s.count('i'))
print("The number of o's are ", s.count('o'))
print("The number of u's are ", s.count('u'))
